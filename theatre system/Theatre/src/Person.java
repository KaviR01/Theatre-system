public class Person {
    String name;
    String surname;
    String email;


    // Constructor that takes name, surname, and email as inputs
    public Person(String name, String surname, String email) {
        this.name = name;
        this.surname = surname;
        this.email = email;
    }

    // Getters for name, surname, and email
    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getEmail() {
        return email;
    }


}

